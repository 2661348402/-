class Solution2 {
    public int minArray(int[] numbers) {
    	int n=numbers.length;
    	int left=0,right=n-1;
    	while(left<=right) {
    		int mid=left+(right-left)/2;
    		if(numbers[mid]<numbers[right]) {
    			right=mid-1;
    		}
    		else if(numbers[mid]>numbers[right]) {
    			left=mid+1;
    		}
    		else {
    			right--;
    		}
    		
    	}
    	return numbers[left];
    }
}